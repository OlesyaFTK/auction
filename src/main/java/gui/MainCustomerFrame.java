    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        pAllLot.removeAll();
        final List<Lot> lots = LotCatalogue.getLotsByName(tfSearch.getText());
        if (lots.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No lots found", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else {
            for (final Lot lot : lots) {
                pAllLot.add(new CustomerLotPanel(lot,user));
            }
        }
        pAllLot.revalidate();
    }//GEN-LAST:event_jButton1ActionPerformed

