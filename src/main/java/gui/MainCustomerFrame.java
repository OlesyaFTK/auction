    public void setLotPanel(){
        List<Lot> lots = LotCatalogue.getConfirmLots();
        for(Lot lot:lots){
            if((lot.getFinishDate().getTimeInMillis() - Calendar.getInstance().getTimeInMillis()) > 0){
                this.pAllLot.add(new CustomerLotPanel(lot,user));
            }
        }
        pAllLot.revalidate();
    }

